"""
第一个 Python 程序 — Hello World
运行: python hello_world.py
"""
# ============================================================
# 1. 基本的 print() 输出
# ============================================================
print("Hello, World!")           # 英文问候
print("你好，世界！")             # 中文问候

# ============================================================
# 2. print() 高级用法 — sep 和 end 参数
# ============================================================
print("Python", "is", "awesome", sep=" | ")   # 自定义分隔符: Python | is | awesome
print("Hello", end="! ")                      # 不换行, 以 "! " 结尾
print("Next line")                            # 接在上一个输出后面

# ============================================================
# 3. 获取用户输入 (input)
# ============================================================
name = input("请输入你的名字: ")
print(f"你好，{name}！欢迎来到 Python 世界！")

# ============================================================
# 4. 注释演示
# ============================================================
# 这是单行注释
print("单行注释用 #")  # 行尾注释也可以

"""
这是多行注释 (实际是字符串字面量,
但未被赋值, 所以 Python 将其忽略)
"""
print("多行注释用三个引号")

print("\n程序执行完毕！")
