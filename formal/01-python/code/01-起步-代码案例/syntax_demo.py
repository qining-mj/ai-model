"""
Python 基础语法演示 — 编码声明、标识符、关键字、缩进、多行语句、注释、输入输出
运行: python syntax_demo.py
"""
# 编码声明已在文件头默认处理 (UTF-8)

# ============================================================
# 1. 标识符 (Identifiers)
# ============================================================
# 标识符: 字母、下划线开头, 后跟字母、数字、下划线
my_var = 10       # 合法
_var = 20         # 合法 (通常表示内部使用)
var_123 = 30      # 合法
# 1var = 40       # 非法 — 不能以数字开头
print(f"标识符示例: my_var={my_var}, _var={_var}, var_123={var_123}")

# ============================================================
# 2. 关键字 (Keywords) — 不能用作标识符
# ============================================================
# Python 有 35+ 个关键字 (保留字)
import keyword
kw_list = keyword.kwlist
print(f"\nPython 关键字 ({len(kw_list)} 个): {kw_list}")
# 常见关键字: False, None, True, and, as, assert, async, await,
#             break, class, continue, def, del, elif, else, except,
#             finally, for, from, global, if, import, in, is, lambda,
#             nonlocal, not, or, pass, raise, return, try, while, with, yield

# ============================================================
# 3. 缩进 (Indentation) — Python 用缩进表示代码块
# ============================================================
if True:
    print("\n缩进示例: 这行前面有 4 个空格")
    print("同一代码块需要相同的缩进")

# ============================================================
# 4. 多行语句 (Multi-line Statements)
# ============================================================
# 使用反斜杠 \ 续行
total = 1 + 2 + 3 \
        + 4 + 5 + 6 \
        + 7 + 8 + 9
print(f"\n多行语句 (反斜杠): total = {total}")

# 在括号内自动续行
long_list = [
    1, 2, 3,
    4, 5, 6
]
print(f"多行语句 (括号内): long_list = {long_list}")

# 多条语句写在同一行 (用 ; 分隔, 不推荐)
x = 1; y = 2; z = 3
print(f"同一行多条语句: x={x}, y={y}, z={z}")

# ============================================================
# 5. 输入输出综合示例
# ============================================================
# 输入 — input() 返回字符串
age_str = input("\n请输入你的年龄: ")

# 类型转换
age = int(age_str)

# 格式化输出 — f-string (推荐)
print(f"你明年将 {age + 1} 岁")

# format() 方法
print("你明年将 {} 岁".format(age + 1))

# % 格式化 (旧式)
print("你明年将 %d 岁" % (age + 1))

print("\n基础语法演示完毕！")
