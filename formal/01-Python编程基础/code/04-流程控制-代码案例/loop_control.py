"""
Python 循环控制演示 — break / continue / pass + 嵌套循环
运行: python loop_control.py
"""

# ============================================================
# 1. break — 跳出循环
# ============================================================
print("=== break ===")
print("  查找第一个偶数:")
for i in range(1, 10):
    if i % 2 == 0:
        print(f"    找到偶数: {i}")
        break
    print(f"    检查 {i}...(不是偶数)")

# ============================================================
# 2. continue — 跳过本次迭代
# ============================================================
print("\n=== continue ===")
print("  打印 1-10 中的奇数:")
for i in range(1, 11):
    if i % 2 == 0:
        continue          # 跳过偶数
    print(f"    {i}")

# ============================================================
# 3. pass — 占位符
# ============================================================
print("\n=== pass ===")
# pass 不做任何事, 用于语法上需要语句但逻辑上空的地方
for i in range(3):
    if i == 1:
        pass    # 未来可能会添加逻辑
    else:
        print(f"  i = {i}, 非 1 分支执行")

# pass 在函数/类定义中也很常用
def not_implemented_yet():
    pass        # TODO: 稍后实现

print("  pass 语句不产生任何操作")

# ============================================================
# 4. 嵌套循环 — 打印乘法表
# ============================================================
print("\n=== 嵌套循环: 乘法表 ===")
for i in range(1, 10):
    for j in range(1, i + 1):
        print(f"{j}x{i}={i*j:<2}", end="  ")
    print()  # 换行

# ============================================================
# 5. break 在嵌套循环中只跳出内层
# ============================================================
print("\n=== break 在嵌套循环中 ===")
print("  当内层 j==3 时 break:")
for i in range(3):
    for j in range(5):
        if j == 3:
            break       # 只跳出内层 j 循环
        print(f"  i={i}, j={j}", end=" | ")
    print()  # 外层继续

print("\n循环控制演示完毕！")
