"""
Python 运算符演示 — 7 大类别 + 优先级
运行: python operators.py
"""

# ============================================================
# 1. 算术运算符
# ============================================================
print("=== 算术运算符 ===")
a, b = 17, 5
print(f"a={a}, b={b}")
print(f"  + 加法: {a + b}")       # 22
print(f"  - 减法: {a - b}")       # 12
print(f"  * 乘法: {a * b}")       # 85
print(f"  / 除法: {a / b}")       # 3.4
print(f"  // 地板除: {a // b}")   # 3
print(f"  % 取余: {a % b}")       # 2
print(f"  ** 幂: {a ** b}")       # 1419857

# ============================================================
# 2. 比较运算符
# ============================================================
print("\n=== 比较运算符 (返回 bool) ===")
print(f"  {a} == {b}: {a == b}")
print(f"  {a} != {b}: {a != b}")
print(f"  {a} > {b}:  {a > b}")
print(f"  {a} < {b}:  {a < b}")
print(f"  {a} >= {b}: {a >= b}")
print(f"  {a} <= {b}: {a <= b}")
# 链式比较
x = 5
print(f"  链式: 1 < {x} < 10 = {1 < x < 10}")

# ============================================================
# 3. 赋值运算符
# ============================================================
print("\n=== 赋值运算符 ===")
x = 10
print(f"  x = {x}")
x += 5;  print(f"  x += 5  -> {x}")   # 15
x -= 3;  print(f"  x -= 3  -> {x}")   # 12
x *= 2;  print(f"  x *= 2  -> {x}")   # 24
x /= 4;  print(f"  x /= 4  -> {x}")   # 6.0
x //= 2; print(f"  x //= 2 -> {int(x)}")  # 3
x %= 2;  print(f"  x %%= 2 -> {int(x)}")  # 1
x **= 3; print(f"  x **= 3 -> {x}")   # 1

# := 海象运算符 (Python 3.8+)
if (n := len("hello")) > 3:
    print(f"  海象运算符: len('hello') = {n}")

# ============================================================
# 4. 位运算符
# ============================================================
print("\n=== 位运算符 ===")
a, b = 12, 5
print(f"  a={a} (二进制 {bin(a)}), b={b} (二进制 {bin(b)})")
print(f"  a & b  = {a & b}  ({bin(a & b)})")   # 按位与
print(f"  a | b  = {a | b}  ({bin(a | b)})")   # 按位或
print(f"  a ^ b  = {a ^ b}  ({bin(a ^ b)})")   # 按位异或
print(f"  ~a     = {~a}  ({bin(~a)})")          # 按位取反
print(f"  a << 1 = {a << 1} ({bin(a << 1)})")   # 左移
print(f"  a >> 1 = {a >> 1} ({bin(a >> 1)})")   # 右移

# ============================================================
# 5. 逻辑运算符 — 短路求值演示
# ============================================================
print("\n=== 逻辑运算符 (短路求值) ===")
print(f"  True and False: {True and False}")
print(f"  True or False:  {True or False}")
print(f"  not True:       {not True}")

# 短路求值 (Short-circuit)
def side_effect():
    print("  -> side_effect() 被调用了!")
    return True

print("  短路演示 (False and side_effect()):")
result = False and side_effect()    # side_effect 不会被调用!
print(f"  结果: {result}")

print("\n  短路演示 (True or side_effect()):")
result = True or side_effect()      # side_effect 不会被调用!
print(f"  结果: {result}")

# ============================================================
# 6. 成员运算符
# ============================================================
print("\n=== 成员运算符 ===")
lst = [1, 2, 3, 4, 5]
print(f"  lst = {lst}")
print(f"  3 in lst:     {3 in lst}")
print(f"  10 not in lst: {10 not in lst}")
print(f"  'H' in 'Hello': {'H' in 'Hello'}")

# ============================================================
# 7. 身份运算符
# ============================================================
print("\n=== 身份运算符 ===")
a = [1, 2, 3]
b = [1, 2, 3]
c = a
print(f"  a = {a}, b = {b}, c = a")
print(f"  a is c:     {a is c}")      # True (同一对象)
print(f"  a is b:     {a is b}")      # False (不同对象, 值相同)
print(f"  a == b:     {a == b}")      # True (值相同)
print(f"  a is not b: {a is not b}")  # True

# ============================================================
# 8. 运算符优先级
# ============================================================
print("\n=== 运算符优先级 ===")
# **  >  * / // %  >  + -
# 括号可以改变优先级
expr1 = 2 + 3 * 4
expr2 = (2 + 3) * 4
print(f"  2 + 3 * 4     = {expr1}")    # 14
print(f"  (2 + 3) * 4   = {expr2}")    # 20

print("\n运算符演示完毕！")
