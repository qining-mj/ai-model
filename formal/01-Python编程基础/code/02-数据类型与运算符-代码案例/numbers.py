"""
Python 数字类型及数学运算演示
运行: python numbers.py
"""

# ============================================================
# 1. int — 任意精度整数
# ============================================================
print("=== int (任意精度) ===")
a = 42
b = 2 ** 100   # 大整数, Python 自动处理
print(f"a = {a}")
print(f"2**100 = {b}")
print(f"位数: {len(str(b))}")

# ============================================================
# 2. float — 浮点数
# ============================================================
print("\n=== float ===")
pi = 3.14159265358979
print(f"pi = {pi}")
print(f"科学计数法: {1.5e3}")          # 1500.0
print(f"精度问题: {0.1 + 0.2}")        # 0.30000000000000004

# ============================================================
# 3. bool — 是 int 的子类
# ============================================================
print("\n=== bool ===")
print(f"True + 1 = {True + 1}")        # 2
print(f"False * 3 = {False * 3}")      # 0
print(f"isinstance(True, int) = {isinstance(True, int)}")

# ============================================================
# 4. complex — 复数
# ============================================================
print("\n=== complex ===")
c1 = 3 + 4j
c2 = complex(1, -2)
print(f"c1 = {c1}, 实部={c1.real}, 虚部={c1.imag}, 模={abs(c1)}")
print(f"c1 + c2 = {c1 + c2}")

# ============================================================
# 5. 除法 / 和 //
# ============================================================
print("\n=== 除法 ===")
print(f"10 / 3 = {10 / 3}")            # 3.333...  (真除法)
print(f"10 // 3 = {10 // 3}")          # 3         (地板除)
print(f"-10 // 3 = {-10 // 3}")        # -4        (向下取整)
print(f"10 % 3 = {10 % 3}")            # 1         (取余)

# ============================================================
# 6. 幂运算 **
# ============================================================
print("\n=== 幂运算 ===")
print(f"2 ** 10 = {2 ** 10}")          # 1024
print(f"9 ** 0.5 = {9 ** 0.5}")        # 3.0 (开平方)

# ============================================================
# 7. 常用数学函数
# ============================================================
print("\n=== 数学函数 ===")
print(f"abs(-5) = {abs(-5)}")
print(f"pow(2, 10) = {pow(2, 10)}")          # 1024
print(f"pow(2, 10, 100) = {pow(2, 10, 100)}")  # (2**10) % 100 = 24
print(f"round(3.14159, 2) = {round(3.14159, 2)}")
print(f"divmod(17, 5) = {divmod(17, 5)}")    # (3, 2) 商和余数

# ============================================================
# 8. 导入 math 模块
# ============================================================
import math
print(f"\nmath.sqrt(16) = {math.sqrt(16)}")
print(f"math.factorial(5) = {math.factorial(5)}")
print(f"math.pi = {math.pi}, math.e = {math.e}")

print("\n数字类型演示完毕！")
